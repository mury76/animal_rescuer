package org.fasttrackit;

public class Animals {

    String name;
    String color;
    String gender;
    int age;
    double weight;
    boolean health;
    String favoriteFoodName;
    String favoriteActivityName;

    public Animals(String name, String color, String gender, int age, double weight) {
        this.name = name;
        this.color = color;
        this.gender = gender;
        this.age = age;
        this.weight = weight;
    }
}
